def unzipGeomFile(geomDict):
    '''
        Checks for gzipped geometry
            - if it finds them,, upzips them and updates the geomDict to point to the unzippe dfile.
            - fidelity does not read in .gz files!
    '''
    print('\n\tChecking for zipped geometry files...')
    global cleanUpGeom
    geomLoc = 'constant/triSurface'
    for geom in geomDict.keys():
        if geom.endswith('.gz'):
            print('\t\tUnzipping geometry file: %s' % (geom))
            source_filepath = os.path.join(geomLoc,geom)
            dest_filepath = os.path.join(geomLoc,geom.replace('.gz',''))
            unzip_gz(source_filepath, dest_filepath) 
            cleanUpGeom.append(geom) # append to the clean up list so that after meshing is done, the unzipped file is removed
    print('\n\t\tUpdating geomDict...')
    for geom in cleanUpGeom:
        print('\t\t\tChanging %s to %s' % (geom,geom.replace('.gz','')))
        geomDict[geom.replace('.gz','')] = geomDict.pop(geom)

    return geomDict



def unzip_gz(source_filepath, dest_filepath):
    """
    Decompresses a .gz file to a specified destination file.

    Args:
        source_filepath (str): Path to the input .gz file.
        dest_filepath (str): Path where the uncompressed file will be saved.
    """
    with gzip.open(source_filepath, 'rb') as f_in:
        with open(dest_filepath, 'wb') as f_out:
            shutil.copyfileobj(f_in, f_out)

def geomToDict(fullCaseSetupDict):
    geomDict = {}
    geomColumnNames = ['scale','refinement','layers','expansion','wallmodel']
    #getting geometry list from geometry section
    try:
        geometryList = fullCaseSetupDict['GEOMETRY']['GEOM']
    except:
        print('ERROR! [GEOMETRY] section invalid!')
        exit()
    
    try:
        print('\t\tGetting geometry...')
        geometryList = geometryList[0].split('\n')
        nGeoms = len(geometryList)
        print('\t\t\tFound: %1.0f' % (nGeoms))
    except:
        print('ERROR! Geometry input invalid!')
        
    #splitting geoms into columns
    for geometry in geometryList:
        geometry = geometry.split(',')
        
        if len(geometry) != len(geomColumnNames) + 1:
            print('ERROR! Geometry input invalid! Insufficient options input for: %s' % (geometry[0]))
            exit()
        else:
            geometryName = geometry[0]
            if geometryName in geomDict.keys():
                print('ERROR! Duplicate geometry name found: %s' % (geometryName))
                exit()
            geomDict[geometryName] = {}
            n = 0
            while n < (len(geomColumnNames)):
                geomCol = geomColumnNames[n]
                geomDict[geometryName][geomCol] = geometry[n+1]
                n = n + 1
    
    
    return geomDict

